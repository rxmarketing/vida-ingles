					<h4>Object pronouns <small>(pronombres tipo objeto)</small></h4>
					
					<p>Los <strong>pronombre tipo objeto</strong> en ingles son 8: </p>
					<ol class="list-inline">
						<li class="list-inline-item">me</li>
						<li class="list-inline-item">you</li>
						<li class="list-inline-item">he</li>
						<li class="list-inline-item">her</li>
						<li class="list-inline-item">it</li>
						<li class="list-inline-item">us</li>
						<li class="list-inline-item">you</li>
						<li class="list-inline-item">them</li>
					</ol>
					<p>Los <em>pronombres de tipo objeto</em> siempre van <b>después</b> del verbo o preposiciones como <i>for, to, with, at</i>.</p>
					<p>Ejemplos:</p>
					<ul class="Ejemplo-lists">
						<li>This message is for <strong>him</strong>. <span class="tradu">Este mensaje es para <em>él</em>.</span></li>
						<li>These books are awesome. I love <strong>them</strong>. <span class="tradu">Estos libros son estupendos. <em>Los</em> amo.</span></li>
						<li>He loves <strong>her</strong> <span class="tradu">Él ama a <em>ella</em>.</span></li>
					</ul>

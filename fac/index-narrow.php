								<h4>Zero conditional</h4>
								<p>Usa el <strong>cero condicional</strong> para expresar lo que sucede en ciertas situaciones. Como todos los condicionales, el cero condicional tiene dos clausulas. La clausula de la <em>condición</em>, que se escribe en presente simple, y la clausula del <em>resultado</em> que también va en presente simple. Ejemplos:</p>
								<ul>
									<li>If I <strong>go</strong> to bed late, I <strong>am</strong> tired in the morning.</li>
									<li>If the weather <strong>is</strong> nice, we usually <strong>go</strong> to the beach.</li>
									<li>If we <strong>don't do</strong> our homework, the teacher <strong>gets</strong> mad.</li>
									<li>If your parents <strong>gives</strong> you pocket money, do you <strong>spend</strong> it on clothes?</li>
								</ul>
								<p>En los ejemplos de arriba comenzamos con la cláusula de la condición y terminamos con la cláusula del resultad. La cláusula de la <em>condición</em> por lo general comienza con <em>If</em>. Pero también podemos comenzar con la cláusula del resultado y terminar con la condición, como muestra los siguientes ejemplos.</p>
								<ul>
									<li>I <strong>am</strong> tired if I <strong>go</strong> to bed late.</li>
									<li>We usually <strong>go</strong> to the beach if the weather <strong>is</strong> nice.</li>
									<li>The teacher <strong>gets</strong> mad if we <strong>don't do</strong> our homework.</li>
									<li>Do you <strong>spend</strong> it on clothes if your parents <strong>give</strong> you pocket money?</li>
								</ul>
								<div class="alert alert-danger">
									<span class="fa fa-warning fa-lg"></span> <small>Si la frase comienza con la <em>condición</em> usa la <em>coma (,)</em> para separar la condición del resultado. Si comienza con el <em>resultado</em> la coma <strong>se omite</strong>.</small> 
								</div>
								<h4>Condicional + imperativo</h4>
								<p>Utiliza <strong>condicional + imperativo</strong> para dar ordenes o instrucciones bajo ciertas condiciones. La cláusula de la condición va en <em>presente simple</em> y la orden o instrucción también va en <em>presente simple</em>. Ejemplos:</p>
								<ul>
									<li>If you <strong>see</strong> John, <strong>tell</strong> him to call his parents.</li>
									<li>If a fire <strong>breaks out</strong>, <strong>call</strong> the fire brigade.</li>
									<li><strong>don't tell</strong> her about the party if you <strong>talk</strong> to Jane.</li>
								</ul>
								<p>Recuerda omitir la <em>coma</em> si comienzas la frase con el resultado.</p>
								<h3><em>when, as soon as, unless</em></h3>
								<p>Tu puedes usar <strong>when, as soon as</strong> y <strong>unless</strong> en lugar de <strong>if</strong> para decir cómo sucede el resultado.</p>
								<p>Usa <strong>when</strong> para decir que el resultado es <em>seguro</em>. Ejemplo:</p>
								<ul>
									<li><strong>When</strong> I eat chocolate, I feel sick.</li>
									<li>I don't go to school <strong>when</strong> it rains.</li>
								</ul>
								<p>Usa <strong>as soon as</strong> para decir que el resultdo es <em>inmediato</em>. Ejemplos:</p>
								<ul>
									<li><strong>As soon as</strong> I get home, I do my homework.</li>
									<li>The students leave the classroom <strong>as soon as</strong> the bell rings.</li>
								</ul>
								<p>Usa <strong>unless</strong> para decir <em>"si no"</em></p>
								<ul>
									<li><strong>Unless</strong> I finish my homework, I don't go out.</li>
									<li>Judith doesn't bring an umbrella <strong>unless</strong> it rains.</li>
								</ul>

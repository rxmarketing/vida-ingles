								<p class="lead">Hablar del futuro con <em>will</em></p>
								<p><i>will</i> es un verbo modal y debe seguirle un verbo en infinitivo.</p>
								<p>Usa <em>will</em> para:</p>
								<ul>
									<li>hacer predicciones</li>
									<li>tomar decisiones</li>
									<li>hacer promesas</li>
									<li>hacer peticiones</li>
									<li>ofrecer algo</li>
									<li>rechazar o negar</li>
								</ul>
								<p>Ejemplos:</p>
								<ul>
									<li>
										Pumas <strong>will win</strong> the Copa MX. <small><i>prediccion</i></small>
									</li>
									<li>
										I <strong>will close</strong> the window. <small><i>decision</i></small>
									</li>
									<li>
										I <strong>will behave</strong> very well. <small><i>promesa</i></small>
									</li>
									<li>
										I <strong>will cary</strong> your bag. <small><i>ofrecer</i></small>
									</li>
									<li>My car <strong>won't start</strong>. It needs repair. <small><i>negar</i></small></li>
									<li><strong>Will</strong> you <strong>take</strong> the rubbish out? <small class="text-muted"><i>petición</i></small></li>
								</ul>
								<p>Su forma negativa es <strong>won't</strong> <i>(will not)</i>.</p>
								<ul>
									<li>It <strong>won't rain</strong> later.</li>
								</ul>
								<p>Usa el adverbio <i>probably</i> para expresar menos probabilidad. Ejemplo:</p>
								<ul>
									<li>I will <strong>probably</strong> stay over tonight.</li>
									<li>I <strong>probably</strong> won't go to Jason's party next week.</li>
								</ul>

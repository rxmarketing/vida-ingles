								<h3>Present simple <small>(verbos regulares e irregulares)</small></h3>
								<p>
									<span itemprop="description">La estructura del <b>presente simple de los verbos regulares e irregulares</b> es completamente diferente a la estructura del <a href="http://vidaingles.com/lecciones/5/presente-simple-del-verbo-to-be" title="Presente simple del verbo be">presente simple del verbo <em>be</em></a>. Si el sujeto de la oracion es una tercera persona singular (he, she, it) <i>el verbo principal se modifica únicamente en la forma positiva</i>. El presente simple de los verbos regulares e irregulares se usa para hablar de hábitos o rutinas.</span>
								</p>
								<h4>
									<span itemprop="description">Presente Simple Positivo</span>
								</h4>
								<p>
									<span itemprop="description">Forma el 
										<b>presente simple positivo</b> con: 
									<code>sujeto + verbo en su forma base + complemento.</code></span>
								</p>
								<p>Ejemplos:</p>
								<ul class="example-lists">
									<li><b>love</b> English.<span class="tradu">Yo <b>amo</b> el Inglés</span></li>
									<li>She <b>washes</b> her hair twice a week. <span class="tradu">Ella <b>lava</b> su cabello dos veces a la semana.</span></li>
									<li>Luis <b>drives</b> his little sister to school everyday. <span class="tradu">Luis <b>conduce</b> a su hermana menor a la escuela todos los días.</span></li>
									<li>Brenda and Rico <b>meet</b> every weekend for coffee. <span class="tradu">Brenda y Rico se <b>reunen</b> cada fin de semana para tomar café.</span></li>
									<li>They <b>do</b> their homework. <span class="tradu">Ellos <b>hacen</b> su tarea.</span></li>
									<li>We <b>speak</b> Spanish. <span class="tradu">Nosotro <b>hablamos</b> Español.</span></li>
								</ul>
								<div class="well">
									<h4>Terceras Personas Singular (he, she, it)</h4>
									<p>Aquí estan los cambios que sufren los verbos cuando el sujeto es una tercera persona. Recuerda; estos cambios se aplican
										solamente a la forma 
									<em>positiva</em> del presente simple.</p>
									<p>Si el sujeto es 
									<em>tercera persona singular</em> (he, she, it) modifica el verbo:</p>
									<ul>
										<li>añadiendo 
										<b>s</b> a la mayoría de los verbos.</li>
										<li style="list-style: none; display: inline">
											<p>Ejemplos:</p>
											<ul>
												<li>She love<b>s</b> English. 
													<span class="tradu">Ella 
													<b>ama</b> el Inglés.</span></li>
													<li>My teacher like<b>s</b> rock music. 
														<span class="tradu">A mi maestro/a le 
														<b>gusta</b> la música rock.</span></li>
											</ul>
										</li>
										<li>si el verbo, en su forma base, terminan con 
											<b>-o, -ch, -sh, -x, -s, -z</b> añade 
										<b>es</b> al final del verbo.</li>
										<li style="list-style: none; display: inline">
											<p>Ejemplos:</p>
											<ul class="example-lists">
												<li>watch -----&gt; watch<b>es</b></li>

								<h3>Subject pronouns</h3>
								<p>Los <b>pronombres personales</b> (subject pronouns) en Inglés son ocho: <b>I, You, He, She, It, We, You, They</b> (personal pronouns)</p>
								<ul class="example-lists">
									<li>Singular: <b>I, You, He, She, It</b></li>
									<li>Plural: <b>We, You, They</b></li>
									<li>Terceras Personas Singular: <b>He, She, It</b></li>
								</ul>
								<p>Siempre debes incluir el sujeto del verbo</p>
								<ul>
									<li><b>Ricardo</b> is my brother. <b>He</b> is good at playing the guitar. <span class="tradu"><b>Ricardo</b> es mi hermano. <b>El</b> es bueno tocando la guitarra.</span></li>
									<li>No: <b>Ricardo</b> is my brother. <del>Is good at playing the guitar.</del></li>
								</ul>
								<p>Siempre escribe <b>I</b> con mayúscula, aun cuando está en medio de la oración</p>
								<ul class="example-lists">
									<li><b>I</b> am an English student.<span class="tradu"><b>Yo</b> soy un estudiante de Inglés.</span></li>
									<li>No: <del><b>i</b> am an English student.</del></li>
									<li><b>I</b> am the man <b>I</b> want to be.</li>
								</ul>
								<p>Utiliza <b>You</b> para singular o plural, (tú, usted o ustedes). Incluye <b>"all"</b> para referirse en plural.</p>
								<ul class="example-lists">
									<li><b>You</b> are very nice.<span class="tradu">Tu/Usted eres/es muy agradable.</span></li>
									<li><b>You</b> <b>all</b> are welcome.<span class="tradu">(<b>Todos</b> <b>ustedes</b> son bienvenidos.)</span></li>
								</ul>
								<p>Utiliza <b>it</b> para objetos o animales</p>
								<ul class="example-lists">
									<li><b>It</b> is a beautiful dress.<span class="tradu"><b>Eso</b> es un lindo vestido.</span></li>
									<li><b>It</b> is a dog.<span class="tradu"><b>Eso</b> es un perro.</span></li>
									<li><b>It</b>'s six o'clock.<span class="tradu">Son las seis.</span></li>
								</ul>
								<p><span class="remember">Recuerda:</span></p>
								<ul class="example-lists">
									<li>siempre escribe <b>I</b> con mayúscula.</li>
									<li>incluye el sujeto del verbo SIEMPRE.</li>
									<li><b>He, She, It</b> son <b>terceras personas singular.</b>.</li>
									<li>son ocho: <b>I</b> (Yo), <b>You</b> (Tu / Usted), <b>He</b> (El), <b>She</b> (Ella), <b>It</b> (Eso / Aquello), <b>We</b> (Nosotros), <b>You</b> (Ustedes), <b>They</b> (Ellos)</li>
								</ul>

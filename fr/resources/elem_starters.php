								<span itemprop="description"><p>Se utiliza el <b>presente contínuo</b> <small>present continuous</small> para expresar una <b>actividad actual en marcha</b>, para expresar una <b>actividad temporal</b> o para expresar un <b>evento futuro planeado</b>.</p><h4>Actividad actual en marcha:</h4><ul class="example-lists">	<li>I <b>am working</b> right now. <span class="tradu">(<b>Estoy trabajando</b> en este momento)</span></li>	<li>My brother <b>is playing</b> his guitar <span class="tradu">(Mi hermano <b>esta tocando</b> su guitarra.)</span></li>	<li>It <b>is raining</b> <span class="tradu"><b>Esta lloviendo</b></span></li></ul><h4>Actividad temporal:</h4><ul class="example-lists">	<li>They <b>are staying</b> at Los Aluxes hotel. <span class="tradu">Ellos se <b>estan alojando</b> en el hotel Los Aluxes.</span></li>	<li>I<b>'m attending</b> a class at UADY. <span class="tradu"><b>Estoy asistiendo</b> a una clase en la UADY.</span></li></ul><h4>Evento futuro planeado.</h4><ul class="example-lists">	<li>Chivas and Pumas <b>are playing</b> this weekend. <span class="tradu">Las Chivas y los Pumas <b>juegan</b> éste fín de semana.</span></li>	<li>I <b>am watching</b> a movie tonight. <span class="tradu"><b>Voy a ver</b> una película esta noche.</span></li>	<li>They <b>are stealing</b> the next presidencial elections. <span class="tradu"><b>Van a robar</b> las próximas elecciones presidenciales.</span></li>	<li>I <b>am going</b> to Kanasín for dinner tonight. <span class="tradu">Yo <b>voy a ir</b> a Kanasín para la cena ésta noche.</span></li></ul>
									<div class="bg-warning">
										<p><strong>OJO:</strong> En Español, no utilizamos el <em>presente contínuo</em> para expresar un evento en el futuro planeado como se hace en Inglés.</p>

			<h3>Object pronouns <small>(pronombres tipo objeto)</small></h3>
			
			<ol class="list-inline">
				<li>me</li>
				<li>you</li>
				<li>he</li>
				<li>her</li>
				<li>it</li>
				<li>us</li>
				<li>you</li>
				<li>them</li>
			</ol>
			<p class="lead">Los <em>pronombres de tipo objeto</em> simpre van <b>despues</b> del verbo o preposiciones como <i>for, to, with, at</i>.</p>
			
			<p>Ejemplos:</p>
			
			<ul class="example-lists">
				<li>This message is for <em>him</em>. <span class="tradu">Este mensaje es para <em>el</em>.</span></li>
				<li>These books are awesome. I love <em>them</em>. <span class="tradu">Estos libros son estupendos. <em>Los</em> amo.</span></li>
				<li>He loves <em>her</em> <span class="tradu">El ama a <em>ella</em>.</span></li>
			</ul>
			

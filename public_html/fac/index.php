			<h3>Future with <em>will</em></h3>
			
			
			
			<p>Usa <em>will</em> para:</p>

			<ul>
					<li>hacer predicciones</li>
					<li>tomar decisiones</li>
					<li>hacer promesas</li>
					<li>hacer peticiones</li>
					<li>ofrecer algo</li>
				</ul>

				<p>Ejemplos:</p>
			
			<ul>
				<li>
					Pumas will win the Copa MX. <small>prediccion</small>
				</li>
				<li>
					I will close the window. <small>decision</small>
				</li>
				<li>
					I will behave very well. <small>promesa</small>
				</li>
				<li>
					I will cary your bag. <small>ofrecer</small>
				</li>
				<li>
					Will you take the rubbish out?. <small class="text-muted">peticion</small>
				</li>
			</ul>
			
			<p>La forma negativa es <em>won't</em>.</p>
			
			<ul>
				<li>It won't rain later.</li>
			</ul>

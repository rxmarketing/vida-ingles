			<h3>Modal verbs <em>may, might, could, must, can</em></h3>
			
			<h3>Expressing present possibility.</h3>	
			
			<p>Use <em>may, might, could</em> to say that something <i>is possible</i>.</p>
			
			<p>Example:</p>
			
			<ul>
				<li>Justin <em>may/might/could</em> leave his job soon. <span class="tradu">(Justin <em>podria</em> dejar su trabajo pronto.)</span></li>
				
			</ul>
			
			<p>Use <em>must</em> to say <i>you are sure</i> about something.</p>
			<p>Example:</p>
			<ul>
				<li>You <em>must</em> be Tina's brother! <span class="tradu">(Tu <em>debes</em> ser el hermano de Tina!)</span></li>
			</ul>
			
			<p>Use <em>can't</em> to say something is <i>impossible</i>.</p>	
			
			<p>Example:</p>
			
			<ul>
				<li>This <em>can't</em> be Rob's car. He's got a Fiat, not a Renault. <span class="tradu">Este <em>no puede ser</em> el coche de Rob. El tiene un Fiat, no un Renault.</span></li>
			</ul>
			
			<p>Use <em>may/might</em> to say something is <i>less certain.</i></p>	
			
			<p>Example:</p>
			
			<ul>
				<li>We <em>may/might</em> go to London this weekend. <span class="tradu">(Nosotros <em>tal vez vayamos</em> a Londres este fin de semana.)</span></li>
			</ul>
			
			<p>Use <em>may, might, could, must, can't + base form</em> of the verb.</p>
			
			<h3>Expressing <em>past possibility</em> with <em>may/might/must/can't + have + past participle</em></h3>
			
			<p>Use <em>could have + past participle</em> to express unrealised past ability. </p>
			<ul>
				<li>Jim <em>could have sent</em> us a text message. <span class="tradu">Jim <em>pudo habernos enviado</em> un mensaje de texto.</span></li>
			</ul>
			
			<p>Use <em>must have + past participle</em> to express probability in the past.</p>
			<ul>
				<li>The plane <em>must have landed</em> early. <span class="tradu">(El avion <em>debio haber aterrizado</em> antes de tiempo.)</span></li>
			</ul>
			
			<p>Usa <em>can't have + past participle</em> to express impossibility in the past.</p>
			
			<ul>
				<li>Lisa <em>can't have left. Her car is in the garage. <span class="tradu">(Lisa <em>no pudo haber salido. Su coche esta en la cochera.)</em></span></em></li>
			</ul>
			

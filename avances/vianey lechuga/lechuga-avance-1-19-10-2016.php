	Articles.
	
		Existen 3 articulos en ingles: "the", "a", "an".
		
		Articulo definido "the"
		
			cosas unicas, que ya se conocen.
			
			the sun
			the teacher
			the apple
			
		Articulo indefinido a/an
		
		cosas en general, cuando no se sabe cual, o no es importante
		
		ejemplos:
		
			I live in "a" flat.
			They play in "a" band.
			an apple
			
	Usa "a" antes de consonantes.
	
		a table

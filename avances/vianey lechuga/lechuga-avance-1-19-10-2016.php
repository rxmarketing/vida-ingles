		ejemplos:
		
			bus ----> buses
			tomato--> tomatoes
			box ----> boxes
			wish ---> wishes
			watch --> watches
			
	3. Si el sustantivo termina en "consonante + y" cambia la "y" a "i" latina y luego a単ade "es".
	

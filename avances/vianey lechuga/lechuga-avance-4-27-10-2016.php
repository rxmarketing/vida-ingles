	Let's (let us)
	
		Usa "let's + verbo (forma base)" para sugerir hacer algo en conjunto.
		
		Ejemplos:
		
			Let's watch a movie.
			
			Let's play Loteria.
			
			Let's not play basketball.
			
			Let's have a party.
	

	

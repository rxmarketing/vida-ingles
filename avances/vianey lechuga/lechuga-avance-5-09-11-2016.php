	Past participles
	
	Forma el pasado participio de los verbos regulares anexando "ed".
	
		talk >>>> talked
		love >>>> loved
		like >>>> liked
		
	if the verbs ends with a single vowel + consonant, doble the consonant.
	
		stop >>> stopped 
		visit >> visited
		travel >> travelled
		
	PRONUNCIATION RULES
	
	pronuncia la "ed" como "t" si el verbo termina (suena) con k,f,p,s,tch,sh 
		
		talked
		laughed
		stopped
		
	pronuncia la "ed" como "id" si terminan en "t", "d"
	
		visited
		started
		decided
		waited
		
	despues de otros sonidos, pronuncia la "ed" como "d"
	
		phoned	(d)
		married
		stayed
	
